#include "libft.h"

void	ft_strtabiter(char **s, void (*f)(char **), int self)
{
	int	i;

	i = 0;
	if (s && (*f))
	{
		while (s[i])
		{
			(*f)(&s[i]);
			i++;
		}
		if (self)
		{
			(*f)((char**)&s);
		}
	}
}
