#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while (i < len)
	{
		if (*src)
			dst[i++] = *(src++);
		else
			dst[i++] = '\0';
	}
	return (dst);
}
