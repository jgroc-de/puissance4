#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	size_t			i;

	if (n == 0)
		return ;
	ptr = s;
	i = n - 1;
	while (i > 0)
		ptr[i--] = '\0';
	ptr[0] = '\0';
}
