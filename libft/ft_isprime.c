#include "libft.h"

int		ft_isprime(int n)
{
	int i;
	int root;

	if (n < 2)
		return (0);
	root = ft_sqrt(n);
	i = 2;
	while (n != i * (n / i) && i <= root)
		i++;
	if (n == i * (n / i))
		return (0);
	return (1);
}
