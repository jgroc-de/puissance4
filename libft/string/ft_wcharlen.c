#include "libft.h"

size_t		ft_wcharlen(int chr)
{
	size_t	i;

	i = 0;
	while (chr)
	{
		chr = chr >> 1;
		i++;
	}
	if (i > 18)
		i = 4;
	else if (i > 12)
		i = 3;
	else if (i > 7)
		i = 2;
	else
		i = 1;
	return (i);
}
