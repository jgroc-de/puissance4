#include "libft.h"

size_t	ft_putwstr(wchar_t *str)
{
	size_t	len;

	len = 0;
	if (str)
	{
		while (*str)
		{
			len += ft_putwchar(*str);
			str++;
		}
	}
	return (len);
}
